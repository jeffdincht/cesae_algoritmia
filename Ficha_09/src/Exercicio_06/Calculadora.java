package Exercicio_06;

public class Calculadora {

    public int sum(int a, int b){
      return   a+b;
    };

    public int sub(int a, int b){
        return a-b;
    };

    public int multiply(int a, int b){
        return a*b;
    };

    public double divide(double a, double b){
        return a/b;
    };

}
